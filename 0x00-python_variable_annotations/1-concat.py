#!/usr/bin/env python3
"""Concatenate Module"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings.

    Parameters:
        str1 (str): The first string to concatenate.
        str2 (str): The second string to concatenate.

    Returns:
        str: The concatenated string of str1 followed by str2.
    """
    return str1 + str2